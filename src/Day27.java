/* Concatenation Of Strings */

import java.util.*;
public class Day27 {
    public static void main(String[] args) {
        // concatenation
        String Firstname = "Ravi";
        String Middlename = "Shankar";
        String Lastname = "Pandey";
        String Fullname = Firstname  + " " + Middlename + " " + Lastname ;
        System.out.println(Fullname);


    }
}